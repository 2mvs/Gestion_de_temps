import * as React from "react"
import { cn } from "@/lib/utils"

const Card = React.forwardRef<
  HTMLDivElement,
  React.HTMLAttributes<HTMLDivElement> & { title?: string; actions?: React.ReactNode }
>(({ className, title, actions, children, ...props }, ref) => (
  <div
    ref={ref}
    className={cn(
      " border border-gray-100 bg-white text-gray-950 shadow",
      className
    )}
    {...props}
  >
    {(title || actions) && (
      <div className="flex flex-col space-y-1.5 p-6">
        <div className="flex items-center justify-between">
          {title && <h3 className="text-2xl font-semibold leading-none tracking-tight text-gray-900">{title}</h3>}
          {actions && <div>{actions}</div>}
        </div>
      </div>
    )}
    <div className={title || actions ? "p-6 pt-6" : "p-6"}>{children}</div>
  </div>
))
Card.displayName = "Card"

